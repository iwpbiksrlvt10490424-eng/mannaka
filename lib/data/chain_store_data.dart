/// Restaurant 名がチェーン店かどうか判定する。
/// 大文字小文字を無視し、部分一致で判定。
bool isChainStoreName(String name) {
  if (name.isEmpty) return false;
  final lowerName = name.toLowerCase();
  for (final chain in kChainStoreKeywords) {
    if (lowerName.contains(chain.toLowerCase())) {
      return true;
    }
  }
  return false;
}
